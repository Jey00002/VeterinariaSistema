    private void cargarMascotas() {
        try {
            Connection cn = new Conexion().getConexion();
            ResultSet rs = cn.prepareStatement("SELECT * FROM mascotas").executeQuery();
            jcbxMascota.removeAllItems();
            
            while(rs.next()) {
                // Creamos el objeto Mascota con sus datos
                Mascota m = new Mascota();
                m.setId_mascota(rs.getInt("id_mascota"));
                m.setNombre(rs.getString("nombre"));
                m.setRaza(rs.getString("raza"));
                
                // Lo agregamos a la lista (se verÃ¡ su nombre gracias al toString)
                jcbxMascota.addItem(m); 
            }
        } catch (Exception e) { e.printStackTrace(); }
    }

    private void cargarServicios() {
        try {
            Connection cn = new Conexion().getConexion();
            ResultSet rs = cn.prepareStatement("SELECT * FROM servicios").executeQuery();
             jcbxServicio.removeAllItems();
            
            while(rs.next()) {
                Servicio s = new Servicio();
                s.setId_servicio(rs.getInt("id_servicio"));
                s.setNombre(rs.getString("nombre"));
                s.setPrecio(rs.getDouble("precio"));
                
                jcbxServicio.addItem(s);
            }
        } catch (Exception e) { e.printStackTrace(); }
    }

