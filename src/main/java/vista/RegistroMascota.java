private void cargarDuenos() {
        try {
            config.Conexion con = new config.Conexion();
            Connection cn = con.getConexion();
            
            // Verificamos conexi√≥n
            if (cn == null) return;

            String sql = "SELECT * FROM duenos";
            PreparedStatement pst = cn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            jcbxDueno.removeAllItems();
            
            while(rs.next()) {
                modelo.Dueno d = new modelo.Dueno();
                d.setId_dueno(rs.getInt("id_dueno"));
                d.setNombre(rs.getString("nombre"));
                d.setApellidos(rs.getString("apellidos"));
                jcbxDueno.addItem(d);
            }
        } catch (Exception e) {
            System.out.println("Error: " + e);
        }
    }

