    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        
        
        if (jtxtNombre.getText().isEmpty() || jtxtFecha.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Completa los datos.");
        return;
    }

    Dueno dueno = (Dueno) jcbxDueno.getSelectedItem();
    if (dueno == null) return;

    try {
        Connection cn = new config.Conexion().getConexion();
        String sql = "INSERT INTO mascotas (nombre, especie, raza, fecha_nacimiento, id_dueno) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = cn.prepareStatement(sql);
        
        pst.setString(1, jtxtNombre.getText());
        pst.setString(2, jcbxEspecie.getSelectedItem().toString());
        pst.setString(3, jtxtRaza.getText());
        pst.setString(4, jtxtFecha.getText());
        pst.setInt(5, dueno.getId_dueno());

        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "¬°Mascota Guardada! üê∂");
        
        jtxtNombre.setText("");
        jtxtRaza.setText("");
        jtxtFecha.setText("");
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jbtnGuardarActionPerformed


